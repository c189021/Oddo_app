/// 약관 문서 한 건 (제목 + 본문).
class TermsDoc {
  const TermsDoc(this.title, this.body);

  final String title;
  final String body;
}

/// 약관 뷰어에 표시할 임시 표준 문구.
///
/// ⚠️ 출시 전 법률 검토를 거친 정식 약관으로 교체 필요 (ROADMAP Phase 8).
/// 항목 제목은 AuthDummy.terms의 title과 일치해야 한다.
abstract final class TermsDummy {
  TermsDummy._();

  static const List<TermsDoc> docs = [
    TermsDoc('서비스 이용약관', '''
제1조 (목적)
이 약관은 Oddo(이하 "서비스")가 제공하는 AI 감정 일기 서비스의 이용 조건과 절차, 이용자와 서비스의 권리·의무 및 책임 사항을 규정함을 목적으로 합니다.

제2조 (서비스의 내용)
서비스는 이용자가 음성으로 기록한 하루의 이야기를 바탕으로 감정 분석, 일기 요약, 감정 리포트, AI 상담 기능을 제공합니다.

제3조 (계정)
이용자는 이메일 또는 소셜 계정으로 가입할 수 있으며, 계정 정보를 정확하게 유지할 책임이 있습니다. 계정은 본인만 사용할 수 있습니다.

제4조 (서비스의 변경·중단)
서비스는 운영상·기술상 필요에 따라 제공 내용을 변경하거나 일시 중단할 수 있으며, 중요한 변경은 사전에 안내합니다.

제5조 (이용자의 의무)
이용자는 관련 법령과 본 약관을 준수해야 하며, 서비스를 부정한 목적으로 이용해서는 안 됩니다.'''),
    TermsDoc('개인정보 처리방침', '''
1. 수집하는 정보
서비스는 가입 시 이메일·닉네임을, 서비스 이용 과정에서 일기 기록·감정 분석 결과를 수집합니다.

2. 이용 목적
수집한 정보는 서비스 제공(일기 저장, 감정 리포트 생성, AI 상담), 계정 관리, 서비스 개선 목적으로만 이용합니다.

3. 보관 및 파기
개인정보는 회원 탈퇴 시 지체 없이 파기합니다. 법령에 따라 보존이 필요한 경우 해당 기간 동안 분리 보관합니다.

4. 제3자 제공
법령에 근거한 경우를 제외하고 이용자의 동의 없이 개인정보를 제3자에게 제공하지 않습니다.

5. 이용자의 권리
이용자는 언제든지 자신의 개인정보를 조회·수정하거나 삭제(탈퇴)를 요청할 수 있습니다.'''),
    TermsDoc('감정 분석 데이터 활용 동의', '''
1. 활용 대상
음성 기록, 표정 데이터, 대화 내용에서 추출한 감정 분석 결과가 활용 대상입니다.

2. 활용 목적
감정 분석 데이터는 이용자 본인의 감정 리포트 생성, 상담 품질 향상, 기준(baseline) 비교 분석에만 사용됩니다.


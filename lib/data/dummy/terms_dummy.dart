2. 선택 동의
본 동의는 선택 사항이며, 동의하지 않아도 서비스 이용에는 제한이 없습니다.

3. 철회 방법
설정의 알림 스위치를 통해 언제든지 수신을 중단할 수 있습니다.'''),
  ];

  /// 제목으로 문서 찾기 (없으면 첫 문서).
  static TermsDoc byTitle(String title) => docs.firstWhere(
        (d) => d.title == title,
        orElse: () => docs.first,
      );
}
